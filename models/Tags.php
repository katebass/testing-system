<?php

namespace app\models;

use Yii;

/**
 * This is the model class for table "tags".
 *
 * @property int $id
 * @property string $name
 *
 * @property QuestionsTags[] $questionsTags
 */
class Tags extends \yii\db\ActiveRecord
{
    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return 'tags';
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['name'], 'required'],
            [['name'], 'string', 'max' => 100],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'name' => 'Name',
        ];
    }

    /**
     * @return \yii\db\ActiveQuery
     */
    public function getQuestionsTags()
    {
        return $this->hasMany(QuestionsTags::className(), ['tag_id' => 'id']);
    }

    public function getQuestions() {
        return $this->hasMany(Tags::className(), ['id' => 'question_id'])
            ->viaTable('questions_tags', ['tag_id' => 'id']);
    }
}
